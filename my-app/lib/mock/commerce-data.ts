/**
 * Returns true when the backend API is not configured or points to localhost.
 * Use this to fall back to mock data during development.
 */
export function isMockMode(): boolean {
  const apiUrl = process.env.NEXT_PUBLIC_API_BASE_URL;
  return !apiUrl || apiUrl === 'http://localhost:8000';
}
